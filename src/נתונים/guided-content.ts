let currentFantasyIndex = -1;
const phaseStepCounters: Record<Phase, number> = { ICE: 0, WARM: 0, HOT: 0, FIRE: 0 };

function getRandomFantasy(): Fantasy {
  if (currentFantasyIndex === -1) {
    currentFantasyIndex = Math.floor(Math.random() * FANTASIES.length);
  }
  return FANTASIES[currentFantasyIndex];
}

export function resetFantasy(): void {
  currentFantasyIndex = -1;
  phaseStepCounters.ICE = 0;
  phaseStepCounters.WARM = 0;
  phaseStepCounters.HOT = 0;
  phaseStepCounters.FIRE = 0;
}

/** Switch to a different random fantasy (not the current one) */
export function switchFantasy(): void {
  if (FANTASIES.length <= 1) return;
  let next: number;
  do {
    next = Math.floor(Math.random() * FANTASIES.length);
  } while (next === currentFantasyIndex);
  currentFantasyIndex = next;
  phaseStepCounters.ICE = 0;
  phaseStepCounters.WARM = 0;
  phaseStepCounters.HOT = 0;
  phaseStepCounters.FIRE = 0;
}

export function getFallbackStep(phase: Phase, index: number, hasToy = false): FallbackContent {
  const fantasy = getRandomFantasy();
  const steps = fantasy.steps[phase];

  // Use the GLOBAL step index passed from JourneyScreen so the fallback
  // never returns the intro of a phase when called mid-session.
  // Also increment internal counter as a defensive backup.
  const stepIdx = (typeof index === 'number' && index >= 0)
    ? index
    : phaseStepCounters[phase];
  phaseStepCounters[phase] = Math.max(phaseStepCounters[phase], stepIdx) + 1;

  const step = steps[stepIdx % steps.length];

  if (hasToy && (phase === 'HOT' || phase === 'FIRE')) {
    return addToyVariation(step, phase, stepIdx);
  }

  return step;
}

export function getCurrentFantasyName(): string {
  if (currentFantasyIndex === -1) return '';
  return FANTASIES[currentFantasyIndex].id;
}

