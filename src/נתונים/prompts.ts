export function buildSystemPrompt(prefs: UserPreferences): string {
  const toyKey = prefs.hasToy ? 'toy' : 'none';

  return `${BASE_SYSTEM_PROMPT}

## הנחיות:
פני אליה ב"את".
${TOY_INSTRUCTIONS[toyKey]}
עוצמה: עדין — שפה עדינה, קצב איטי, הנחיות הדרגתיות. לא לדחוף.`;
}

