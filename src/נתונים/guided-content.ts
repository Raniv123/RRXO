export const FALLBACK_CONTENT: Record<Phase, FallbackContent[]> = {
  ICE: ICE_CONTENT,
  WARM: WARM_CONTENT,
  HOT: HOT_CONTENT,
  FIRE: FIRE_CONTENT,
};

// Shuffle within phase for unpredictability (keep first item fixed as intro)
export function getShuffledSteps(phase: Phase): FallbackContent[] {
  const content = [...FALLBACK_CONTENT[phase]];
  if (content.length <= 2) return content;
  const first = content[0];
  const rest = content.slice(1);
  // Fisher-Yates shuffle
  for (let i = rest.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [rest[i], rest[j]] = [rest[j], rest[i]];
  }
  return [first, ...rest];
}

export function getFallbackStep(phase: Phase, index: number): FallbackContent {
  const content = FALLBACK_CONTENT[phase];
  return content[index % content.length];
}
